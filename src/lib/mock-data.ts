// 関連作品（モック版: ジャンル一致+タグ重複で類似度計算）
export function getMockRelatedNovels(
  novel: { id: string; genre: string; tags: string[] },
  limit: number = 3
) {
  return MOCK_NOVELS
    .filter((n) => n.id !== novel.id)
    .map((n) => {
      const genreBonus = n.genre === novel.genre ? 5 : 0;
      const tagOverlap = n.tags.filter((t) => novel.tags.includes(t)).length;
      return { novel: n, similarity: genreBonus + tagOverlap * 2 };
    })
    .sort((a, b) => b.similarity - a.similarity)
    .slice(0, limit)
    .map((s) => s.novel);
}
