function main(): void {
  const taxonomyPath = "data/generation/genre-taxonomy.json";
  const outPath = "data/generation/element-grid.json";
  const taxonomy = JSON.parse(readFileSync(taxonomyPath, "utf-8")) as Taxonomy;

  // 旧 element-grid をバックアップ読み込み(マージ用)
  let oldGrid: Record<string, { 境遇: string[]; 転機: string[]; 方向: string[]; フック: string[] }> = {};
  try {
    const old = JSON.parse(readFileSync(outPath, "utf-8"));
    oldGrid = old.byGenre ?? {};
  } catch { /* なくてもOK */ }

  const newGrid: Record<string, { 境遇: string[]; 転機: string[]; 方向: string[]; フック: string[] }> = {};

  for (const major of taxonomy.majorGenres) {
    for (const sub of major.subGenres) {
      const axes = SUBGENRE_AXES[sub.id];
      if (!axes) {
        console.warn(`⚠️ ${sub.id} の4軸辞書が未定義。スキップ`);
        continue;
      }
      newGrid[sub.id] = {
        境遇: [...new Set(axes.境遇)],
        転機: [...new Set(axes.転機)],
        方向: [...new Set(axes.方向)],
        フック: [...new Set(axes.フック)],
      };
    }
  }

  // 旧キーも残す(後方互換)
  for (const [key, val] of Object.entries(oldGrid)) {
    if (!newGrid[key]) {
      newGrid[key] = val;
    }
  }

  const out = { byGenre: newGrid };
  writeFileSync(outPath, JSON.stringify(out, null, 2));

  console.log(`✅ element-grid.json v2 を生成: ${Object.keys(newGrid).length}キー`);
  for (const [g, axes] of Object.entries(newGrid)) {
    if (SUBGENRE_AXES[g]) {
      console.log(`  ${g}: 境遇${axes.境遇.length} 転機${axes.転機.length} 方向${axes.方向.length} フック${axes.フック.length} [NEW]`);
    }
  }
}

main();
